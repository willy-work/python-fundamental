jumlah_buku = 10
print("perintah ibu 'baca seluruh buku'")

jumlah_buku_yang_sudah_di_baca = 0
print(f"jumlah_buku_yang_sudah_di_baca {jumlah_buku_yang_sudah_di_baca}")

